import { makeScript } from "scripts/helpers";

import { getDriveFileId } from "@connectors/connectors/google_drive/temporal/utils";
import { GoogleDriveFiles } from "@connectors/lib/models/google_drive";

async function doIt({
  connectorId,
  fileIds,
}: {
  connectorId: number;
  fileIds: string[];
}) {
  for (const fileId of fileIds) {
    const existingFile = await GoogleDriveFiles.findOne({
      where: {
        dustFileId: fileId,
        connectorId: connectorId,
      },
    });
    if (existingFile) {
      await existingFile.update({
        skipReason: "blacklisted",
      });
    } else {
      await GoogleDriveFiles.create({
        driveFileId: getDriveFileId(fileId),
        dustFileId: fileId,
        name: "unknown",
        mimeType: "unknown",
        connectorId: connectorId,
        skipReason: "blacklisted",
      });
    }
  }
}

